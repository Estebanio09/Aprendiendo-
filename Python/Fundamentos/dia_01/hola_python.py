print(2 + 3)   # Suma (+)
print(3 - 1)   # Resta (-)
print(2 * 3)   # Multiplicación (*)
print(3 / 2)   # División (/)
print(3 ** 2)  # Exponencial (**)
print(3 % 2)   # Módulo (%), que devuelve el residuo de la división
print(3 // 2)  # División entera (//), que redondea hacia abajo el resultado


print(type(5)) # Entero (Int)
print(type(5.3)) # Flotante (Float)
print(type(5 + 3j)) # Número complejo (Complex)
print(type(True)) # Booleans (bool)
print(type("Soy Esteban")) # Cadena de texto (String)
#Estructuras de datos 
print(type({'name':'Asabeneh'})) # Diccionario (Dictionary)
print(type({9.8, 3.14, 2.7}))    # Conjunto (Set)
print(type((9.8, 3.14, 2.7)))    # Tupla (Tuple)


